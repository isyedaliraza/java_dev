/**
 * In java we have three types of comments:
 * 1. Single line comments
 * 2. Multi line comments
 * 3. Javadoc comments
 * 
 * This javadoc comment will appear in the documentation.
 * We can generate a documentation of our code and javadoc will
 * output it as html file which can be viewed in our browser.
 */
public class Comments {
    /*
        Example of a multi-line comment.
        This app prints hello world to the console.
     */

    /**
     * This is the entry point of our application
     * @param args
     * @return void
     */
    public static void main(String[] args) {
        System.out.println("Hello, world!");
    } // end of the main method - Example of single line comment
} // end of the class
